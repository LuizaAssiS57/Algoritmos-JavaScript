let X1 = prompt("Digite o valor inicial X1: ")
let X2 = prompt("Digite o valor final X2: ")
let Y1 = prompt("Digite o valor inicial Y1: ")
let Y2 = prompt("Digite o valor final Y2: ")

let subtracaoX = (X2 - X1)
let subtracaoY = (Y2 = Y1)
let resultado = Math.sqrt(Math.pow(subtracaoX, 2) + Math.pow(subtracaoY, 2))

document.write(resultado.toFixed(2))